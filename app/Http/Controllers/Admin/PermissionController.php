     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        if($request->ajax()) {
            $data = Permission::all();

            return DataTables::of($data)
                ->addIndexColumn()
                ->addColumn('action', '_admin.user-management.permission.action')
                ->make();
        }

        return view('_admin.user-management.permission.index');
    }

